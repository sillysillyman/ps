#include <cstdio>

int main() {
  long long N;
  long long arr[20] = {1, 1, 2, 6, 24, 120, 720, 5040, 40320, 362880, 3628800, 39916800, 479001600, 6227020800, 87178291200, 1307674368000, 20922789888000, 355687428096000, 6402373705728000, 121645100408832000};
  scanf("%lld", &N);
  if (!N) return printf("NO"), 0;
  for (int i = 19; i >= 0; i--) if (N >= arr[i]) N -= arr[i];
  printf(N ? "NO" : "YES");
}